#include "ch32v30x_rcc.h"

/* PWR寄存器位掩码 */
/* 控制寄存器（CTLR）位掩码 */
#define CTLR_DS_MASK     ((uint32_t)0xFFFFFFFC)  /* 用于清除掉电深度睡眠模式位（第1和0位） */
#define CTLR_PLS_MASK    ((uint32_t)0xFFFFFF1F)  /* 用于清除电源电压检测器（PVD）级别选择位（第7:5位） */

/*********************************************************************
 * 函数名称：PWR_DeInit
 *
 * 功能描述：将PWR外设寄存器复位到默认的初始值。
 *
 * 输入参数：无
 *
 * 返回参数：无
 *
 * 注意事项：通过RCC（复位和时钟控制）模块的APB1总线复位功能来实现PWR模块的复位。
 *********************************************************************/
void PWR_DeInit(void)
{
    RCC_APB1PeriphResetCmd(RCC_APB1Periph_PWR, ENABLE);   /* 使能PWR模块的复位 */
    RCC_APB1PeriphResetCmd(RCC_APB1Periph_PWR, DISABLE);  /* 禁用PWR模块的复位（完成复位操作） */
}

/*********************************************************************
 * 函数名称：PWR_BackupAccessCmd
 *
 * 功能描述：使能或禁用对RTC和备份寄存器的访问。
 *
 * 输入参数：NewState - 访问RTC和备份寄存器的新状态，可以是ENABLE或DISABLE。
 *
 * 返回参数：无
 *
 * 注意事项：此功能通常在操作RTC或备份寄存器前调用，以确保对这些区域的写访问权限。
 *********************************************************************/
void PWR_BackupAccessCmd(FunctionalState NewState)
{
    if(NewState)  /* 判断是否需要使能访问 */
    {
        PWR->CTLR |= (1 << 8);  /* 设置CTLR寄存器的第8位（备份区域访问使能位）为1，使能访问 */
    }
    else
    {
        PWR->CTLR &= ~(1 << 8); /* 清除CTLR寄存器的第8位，禁止访问 */
    }
}

/*********************************************************************
 * 函数名称：PWR_PVDCmd
 *
 * 功能描述：使能或禁用电源电压检测器（PVD）。
 *
 * 输入参数：NewState - PVD的新状态（ENABLE或DISABLE）。
 *
 * 返回参数：无
 *
 * 注意事项：PVD用于监控VDD电源电压，当电压低于设定的阈值时会产生中断或事件。
 *********************************************************************/
void PWR_PVDCmd(FunctionalState NewState)
{
    if(NewState)  /* 判断是否需要使能PVD */
    {
        PWR->CTLR |= (1 << 4);  /* 设置CTLR寄存器的第4位（PVD使能位）为1，使能PVD */
    }
    else
    {
        PWR->CTLR &= ~(1 << 4); /* 清除CTLR寄存器的第4位，禁用PVD */
    }
}

/*********************************************************************
 * 函数名称：PWR_PVDLevelConfig
 *
 * 功能描述：配置电源电压检测器（PVD）的电压检测阈值。
 *
 * 输入参数：PWR_PVDLevel - 指定PVD的检测级别，可选值如下：
 *            PWR_PVDLevel_MODE0 - PVD检测级别设置为模式0（对应特定电压阈值，详见数据手册）
 *            PWR_PVDLevel_MODE1 - PVD检测级别设置为模式1
 *            PWR_PVDLevel_MODE2 - PVD检测级别设置为模式2
 *            PWR_PVDLevel_MODE3 - PVD检测级别设置为模式3
 *            PWR_PVDLevel_MODE4 - PVD检测级别设置为模式4
 *            PWR_PVDLevel_MODE5 - PVD检测级别设置为模式5
 *            PWR_PVDLevel_MODE6 - PVD检测级别设置为模式6
 *            PWR_PVDLevel_MODE7 - PVD检测级别设置为模式7
 *
 * 返回参数：无
 *
 * 注意事项：不同的模式对应不同的电压阈值，具体电压值请参考芯片数据手册。配置时先清除相关位再设置。
 *********************************************************************/
void PWR_PVDLevelConfig(uint32_t PWR_PVDLevel)
{
    uint32_t tmpreg = 0;        /* 定义临时变量用于操作寄存器 */
    tmpreg = PWR->CTLR;         /* 读取当前CTLR寄存器的值到临时变量 */
    tmpreg &= CTLR_PLS_MASK;    /* 使用掩码清除PVD级别选择位（第7:5位） */
    tmpreg |= PWR_PVDLevel;     /* 将新的PVD级别设置到临时变量中 */
    PWR->CTLR = tmpreg;         /* 将临时变量的值写回CTLR寄存器，完成配置 */
}

/*********************************************************************
 * 函数名称：PWR_WakeUpPinCmd
 *
 * 功能描述：使能或禁用唤醒引脚功能。
 *
 * 输入参数：NewState - 唤醒引脚功能的新状态（ENABLE或DISABLE）。
 *
 * 返回参数：无
 *
 * 注意事项：唤醒引脚（通常为PA0）可用于从待机模式唤醒MCU。使能后，该引脚上的上升沿可以唤醒系统。
 *********************************************************************/
void PWR_WakeUpPinCmd(FunctionalState NewState)
{
    if(NewState)  /* 判断是否需要使能唤醒引脚功能 */
    {
        PWR->CSR |= (1 << 8);   /* 设置CSR寄存器的第8位（唤醒引脚使能位）为1，使能功能 */
    }
    else
    {
        PWR->CSR &= ~(1 << 8);  /* 清除CSR寄存器的第8位，禁用功能 */
    }
}

/*********************************************************************
 * 函数名称：PWR_EnterSTOPMode
 *
 * 功能描述：进入停止（STOP）模式。
 *
 * 输入参数：
 *          PWR_Regulator - 指定停止模式下的稳压器状态。
 *            PWR_Regulator_ON - 停止模式且稳压器保持开启（功耗较高，唤醒较快）
 *            PWR_Regulator_LowPower - 停止模式且稳压器处于低功耗模式（功耗较低，唤醒稍慢）
 *          PWR_STOPEntry - 指定通过WFI（等待中断）或WFE（等待事件）指令进入停止模式。
 *            PWR_STOPEntry_WFI - 使用WFI指令进入停止模式
 *            PWR_STOPEntry_WFE - 使用WFE指令进入停止模式
 *
 * 返回参数：无
 *
 * 注意事项：
 *         1. 在停止模式下，内核时钟停止，但SRAM和寄存器内容保持不变。
 *         2. 任何外部中断（包括唤醒引脚中断）都可以唤醒MCU。
 *         3. 进入停止模式前，需要配置好唤醒源（如外部中断）。
 *         4. 函数中操作了NVIC的SCTLR寄存器以进行必要的系统控制（如休眠深度）。
 *********************************************************************/
void PWR_EnterSTOPMode(uint32_t PWR_Regulator, uint8_t PWR_STOPEntry)
{
    uint32_t tmpreg = 0;        /* 定义临时变量用于操作寄存器 */
    tmpreg = PWR->CTLR;         /* 读取当前CTLR寄存器的值 */
    tmpreg &= CTLR_DS_MASK;     /* 使用掩码清除掉电深度睡眠模式位（第1和0位） */
    tmpreg |= PWR_Regulator;    /* 设置稳压器状态位 */
    PWR->CTLR = tmpreg;         /* 写回CTLR寄存器，配置停止模式的功耗特性 */

    NVIC->SCTLR |= (1 << 2);    /* 设置SCTLR寄存器的第2位，准备进入低功耗模式（具体作用参考内核手册） */

    if(PWR_STOPEntry == PWR_STOPEntry_WFI)  /* 判断使用哪种指令进入停止模式 */
    {
        __WFI();                /* 执行WFI指令，进入停止模式，等待中断唤醒 */
    }
    else
    {
        __WFE();                /* 执行WFE指令，进入停止模式，等待事件唤醒 */
    }

    NVIC->SCTLR &= ~(1 << 2);   /* 清除SCTLR寄存器的第2位，退出低功耗模式配置 */
}

/*********************************************************************
 * 函数名称：PWR_EnterSTANDBYMode
 *
 * 功能描述：进入待机（STANDBY）模式。
 *
 * 输入参数：无
 *
 * 返回参数：无
 *
 * 注意事项：
 *         1. 在待机模式下，除了备份域（RTC、备份寄存器）和待机电路，整个VDD域都会被断电。
 *            SRAM和寄存器内容会丢失（除非使用特定的RAM保持功能）。
 *         2. 唤醒后，程序会从复位向量重新开始执行（相当于一次复位，但某些标志位可以指示唤醒源）。
 *         3. 可以通过唤醒引脚、RTC闹钟、外部复位等唤醒。
 *         4. 函数中先清除唤醒标志，然后设置掉电深度睡眠位，最后通过WFI指令进入待机模式。
 *********************************************************************/
void PWR_EnterSTANDBYMode(void)
{
    PWR->CTLR |= PWR_CTLR_CWUF; /* 设置CTLR寄存器的CWUF位（清除唤醒标志），为进入待机模式做准备 */
    PWR->CTLR |= PWR_CTLR_PDDS; /* 设置CTLR寄存器的PDDS位（掉电深度睡眠选择），选择进入待机模式 */
    NVIC->SCTLR |= (1 << 2);    /* 设置SCTLR寄存器的第2位，准备进入低功耗模式 */

    __WFI();                    /* 执行WFI指令，进入待机模式 */
}

/*********************************************************************
 * 函数名称：PWR_GetFlagStatus
 *
 * 功能描述：检查指定的PWR标志是否被设置。
 *
 * 输入参数：PWR_FLAG - 指定要检查的标志，可以是以下值之一：
 *            PWR_FLAG_WU   - 唤醒标志，表示发生了唤醒事件
 *            PWR_FLAG_SB   - 待机标志，表示从待机模式唤醒
 *            PWR_FLAG_PVDO - PVD输出标志，表示PVD检测到VDD低于/高于设定阈值（具体看配置）
 *
 * 返回参数：标志的新状态（SET或RESET）。
 *
 * 注意事项：此函数读取PWR状态寄存器（CSR）中的相应标志位，并返回其状态。
 *********************************************************************/
FlagStatus PWR_GetFlagStatus(uint32_t PWR_FLAG)
{
    FlagStatus bitstatus = RESET;  /* 定义标志状态变量，默认为RESET（0） */

    if((PWR->CSR & PWR_FLAG) != (uint32_t)RESET)  /* 检查CSR寄存器中对应的标志位是否为1 */
    {
        bitstatus = SET;           /* 如果标志位为1，则返回SET（1） */
    }
    else
    {
        bitstatus = RESET;         /* 如果标志位为0，则返回RESET（0） */
    }
    return bitstatus;              /* 返回标志状态 */
}

/*********************************************************************
 * 函数名称：PWR_ClearFlag
 *
 * 功能描述：清除PWR的挂起标志。
 *
 * 输入参数：PWR_FLAG - 指定要清除的标志，可以是以下值之一：
 *            PWR_FLAG_WU - 唤醒标志
 *            PWR_FLAG_SB - 待机标志
 *
 * 返回参数：无
 *
 * 注意事项：通过向CTLR寄存器的对应位写1来清除CSR寄存器中的标志位（注意：不是直接写CSR）。
 *           具体实现中，将标志位左移2位后写入CTLR的相应清除位。
 *********************************************************************/
void PWR_ClearFlag(uint32_t PWR_FLAG)
{
    PWR->CTLR |= PWR_FLAG << 2;  /* 将标志位左移2位后与CTLR寄存器进行或操作，以清除对应的标志位 */
}

/*********************************************************************
 * 函数名称：PWR_EnterSTANDBYMode_RAM
 *
 * 功能描述：进入待机模式，并开启RAM数据保持功能（2K+30K RAM保持供电）。
 *
 * 输入参数：无
 *
 * 返回参数：无
 *
 * 注意事项：
 *         1. 此模式在普通待机模式的基础上，增加了对部分RAM（2K和30K）的供电，使其内容在待机期间不丢失。
 *         2. 唤醒后，保持的RAM数据可以继续使用，但程序仍从复位向量重新执行。
 *         3. 函数中通过设置CTLR寄存器的第16和17位来使能2K和30K RAM在待机模式下的供电。
 *         4. 其他部分与普通待机模式类似：清除唤醒标志、选择待机模式、设置内核休眠控制、执行WFI指令。
 *********************************************************************/
void PWR_EnterSTANDBYMode_RAM(void)
{
    uint32_t tmpreg = 0;          /* 定义临时变量 */
    tmpreg = PWR->CTLR;           /* 读取当前CTLR寄存器的值 */

    tmpreg |= PWR_CTLR_CWUF;      /* 设置清除唤醒标志位 */
    tmpreg |= PWR_CTLR_PDDS;      /* 设置掉电深度睡眠选择位，选择待机模式 */

    /* 使能2K和30K RAM在待机模式下的供电（保持数据） */
    tmpreg |= (0x1 << 16) | (0x1 << 17);  /* 设置第16位（2K RAM保持）和第17位（30K RAM保持） */

    PWR->CTLR = tmpreg;           /* 将配置写回CTLR寄存器 */

    NVIC->SCTLR |= (1 << 2);      /* 设置内核休眠控制寄存器 */

    __WFI();                      /* 执行WFI指令进入待机模式 */
}

/*********************************************************************
 * 函数名称：PWR_EnterSTANDBYMode_RAM_LV
 *
 * 功能描述：进入待机模式，并开启RAM数据保持功能和低电压（LV）模式（2K+30K RAM保持供电且低电压）。
 *
 * 输入参数：无
 *
 * 返回参数：无
 *
 * 注意事项：
 *         1. 在PWR_EnterSTANDBYMode_RAM的基础上，额外使能了低电压模式，进一步降低功耗。
 *         2. 低电压模式通过设置CTLR寄存器的第20位来使能。
 *         3. 适用于对功耗要求极高的应用场景。
 *********************************************************************/
void PWR_EnterSTANDBYMode_RAM_LV(void)
{
    uint32_t tmpreg = 0;
    tmpreg = PWR->CTLR;

    tmpreg |= PWR_CTLR_CWUF;
    tmpreg |= PWR_CTLR_PDDS;

    /* 使能2K和30K RAM在待机模式下的供电（保持数据） */
    tmpreg |= (0x1 << 16) | (0x1 << 17);
    /* 使能低电压（LV）模式 */
    tmpreg |= (0x1 << 20);        /* 设置第20位（低电压模式使能） */

    PWR->CTLR = tmpreg;

    NVIC->SCTLR |= (1 << 2);

    __WFI();
}

/*********************************************************************
 * 函数名称：PWR_EnterSTANDBYMode_RAM_VBAT_EN
 *
 * 功能描述：进入待机模式，并开启RAM数据保持功能（VBAT使能，即通过备用电池供电保持2K+30K RAM）。
 *
 * 输入参数：无
 *
 * 返回参数：无
 *
 * 注意事项：
 *         1. 此模式用于当主电源VDD掉电时，通过备用电池（VBAT）为部分RAM（2K和30K）供电，保持数据。
 *         2. 通过设置CTLR寄存器的第18和19位来使能VBAT对2K和30K RAM的供电。
 *         3. 需要硬件上连接备用电池，并且VBAT引脚供电正常。
 *********************************************************************/
void PWR_EnterSTANDBYMode_RAM_VBAT_EN(void)
{
    uint32_t tmpreg = 0;
    tmpreg = PWR->CTLR;

    tmpreg |= PWR_CTLR_CWUF;
    tmpreg |= PWR_CTLR_PDDS;

    /* 使能VBAT对2K和30K RAM的供电（在待机模式下通过备用电池保持数据） */
    tmpreg |= (0x1 << 18) | (0x1 << 19);  /* 设置第18位（2K RAM VBAT保持）和第19位（30K RAM VBAT保持） */

    PWR->CTLR = tmpreg;

    NVIC->SCTLR |= (1 << 2);

    __WFI();
}

/*********************************************************************
 * 函数名称：PWR_EnterSTANDBYMode_RAM_LV_VBAT_EN
 *
 * 功能描述：进入待机模式，并开启RAM数据保持功能、低电压模式和VBAT供电（VBAT使能）。
 *
 * 输入参数：无
 *
 * 返回参数：无
 *
 * 注意事项：
 *         1. 结合了RAM保持、低电压和VBAT供电三种功能，实现最低功耗的数据保持待机模式。
 *         2. 适用于主电源可能断开，由备用电池供电并需要极低功耗保持RAM数据的场景。
 *         3. 依次使能了清除唤醒标志、待机模式、VBAT保持RAM、低电压模式。
 *********************************************************************/
void PWR_EnterSTANDBYMode_RAM_LV_VBAT_EN(void)
{
    uint32_t tmpreg = 0;
    tmpreg = PWR->CTLR;

    tmpreg |= PWR_CTLR_CWUF;
    tmpreg |= PWR_CTLR_PDDS;

    /* 使能VBAT对2K和30K RAM的供电 */
    tmpreg |= (0x1 << 18) | (0x1 << 19);
    /* 使能低电压（LV）模式 */
    tmpreg |= (0x1 << 20);

    PWR->CTLR = tmpreg;

    NVIC->SCTLR |= (1 << 2);

    __WFI();
}

/*********************************************************************
 * 函数名称：PWR_EnterSTOPMode_RAM_LV
 *
 * 功能描述：进入停止模式，并开启RAM数据保持功能和低电压模式。
 *
 * 输入参数：
 *          PWR_Regulator - 指定停止模式下的稳压器状态（目前仅支持低功耗模式，即PWR_Regulator_LowPower）。
 *          PWR_STOPEntry - 指定通过WFI或WFE指令进入停止模式。
 *
 * 返回参数：无
 *
 * 注意事项：
 *         1. 此函数在普通停止模式的基础上，增加了低电压模式，以进一步降低功耗，同时保持所有RAM数据。
 *         2. 停止模式下，CPU时钟停止，但SRAM和寄存器内容全部保持，唤醒后程序从停止处继续执行。
 *         3. 低电压模式通过设置CTLR寄存器的第20位来使能。
 *         4. 函数流程与PWR_EnterSTOPMode类似，但增加了低电压模式的设置。
 *********************************************************************/
void PWR_EnterSTOPMode_RAM_LV(uint32_t PWR_Regulator, uint8_t PWR_STOPEntry)
{
    uint32_t tmpreg = 0;
    tmpreg = PWR->CTLR;
    tmpreg &= CTLR_DS_MASK;       /* 清除掉电深度睡眠模式位 */
    tmpreg |= PWR_Regulator;      /* 设置稳压器状态（应为低功耗模式） */

    tmpreg |= (0x1 << 20);        /* 使能低电压（LV）模式 */

    PWR->CTLR = tmpreg;           /* 配置停止模式并开启低电压模式 */

    NVIC->SCTLR |= (1 << 2);      /* 设置内核休眠控制寄存器 */

    if(PWR_STOPEntry == PWR_STOPEntry_WFI)
    {
        __WFI();
    }
    else
    {
        __WFE();
    }

    NVIC->SCTLR &= ~(1 << 2);     /* 清除内核休眠控制位 */
}
